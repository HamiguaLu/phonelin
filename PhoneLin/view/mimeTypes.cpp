QString MimeTypes::getMimeTypes(QString sFileName) {
    QString sDefaultMimeType = "unknow";
    int iExtPos = sFileName.lastIndexOf(".");
    if (iExtPos <= 0)
    {
        return sDefaultMimeType;
    }

    iExtPos = sFileName.length() - iExtPos;
    QString sExt = sFileName.right(iExtPos + 1);

    if (!m_map.contains(sExt))
    {
        return sDefaultMimeType;
    }

    QMap<QString,QString>::iterator it = m_map.find(sExt); //找到特定的“键-值”对
    return it.value();


}
